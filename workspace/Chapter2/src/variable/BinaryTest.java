package variable;

public class BinaryTest {

	public static void main(String[] args) {
		
		int num = 10;
		int bNum = 0B1010;
		int oNum = 012;
		int oxNum = 0xA; // 0x, 0X 둘 다 가능
		
		System.out.println(num);
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(oxNum);
		
	}

}
